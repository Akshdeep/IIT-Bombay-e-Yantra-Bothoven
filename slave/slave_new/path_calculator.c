/*
* Function Name:	find_path
* Input:			source node, destination node
* Output:			void
* Logic:			calculates the shortest the shortest path and stores it in the global path array
*
* Example Call:	find_path(source, destination)
*
*/
void find_path(int source, int destination) {
	int u;
	shortest_distance = 0;
	int tmp = 0;
	while (destination != source) {
		path[tmp++] = destination;
		u = predecessor[destination];
		shortest_distance = shortest_distance + (int) connected_nodes[u][destination];
		destination = u;
		path_length++;
	}

	path[tmp] = source;

}

/*
* Function Name:	dijkstra
* Input:			source node
* Output:			void
* Logic:			using dijkstra's algorithm for finding the path
*
* Example Call:	find_path(source, destination)
*
*/
void dijkstra(int source) {
	int count, current;
	for (count = 0; count < total_nodes; count++) {
		predecessor[count] = NIL;
		pathLength[count] = infinity;
		node_status[count] = TEMPORARY;
	}
	pathLength[source] = 0;
	while (1) {
		current = min_tmp();
		if (current == NIL) {
			return;
		}
		node_status[current] = PERMANENT;
		for (count = 0; count < total_nodes; count++) {
			if (connected_nodes[current][count] != '0' && node_status[count] == TEMPORARY) {
				if (pathLength[current] + (int) connected_nodes[current][count] < pathLength[count]) {
					predecessor[count] = current;
					pathLength[count] = pathLength[current] + (int) connected_nodes[current][count];
				}
			}
		}
	}
}

int min_tmp() {
	int count;
	int min = infinity;
	int x = NIL;
	for (count = 0; count < total_nodes; count++) {
		if (node_status[count] == TEMPORARY && pathLength[count] < min) {
			min = pathLength[count];
			x = count;
		}
	}
	return x;
}